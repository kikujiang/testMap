package map.test.testmap.model;

/**
 * 任务统计信息
 * {"result":1,"object":{"checkSum":0,"checkAdd":0,"publishSum":0,"publishAdd":0}}
 */
public class TaskInfo {
    private int checkSum;
    private int checkAdd;
    private int publishSum;
    private int publishAdd;

    public int getCheckSum() {
        return checkSum;
    }

    public int getCheckAdd() {
        return checkAdd;
    }

    public int getPublishSum() {
        return publishSum;
    }

    public int getPublishAdd() {
        return publishAdd;
    }
}
